export const forms = (state = initialState, action): Hackathon => {
  switch (action.type) {
    case 'UPDATE_FIELD': {
      const form = Object.assign({}, state[action.form], { [action.field]: action.value });
      return Object.assign({}, state, { [action.form]: form });
    }
    case 'ERROR': {
      const error = Object.assign({}, state[action.form].error, { [action.field]: action.value });
      const form = Object.assign({}, state[action.form], { error });
      return Object.assign({}, state, { [action.form]: form });
    }
    case 'CHANGE_PROVIDER': {
      const emailPlaceholder = action.provider === 'google'
        ? 'Your Google Email Address'
        : 'Your Email Address';

      const passwordPlaceholder = action.provider === 'google'
        ? 'Your Google Password'
        : 'Your PTC Password';

      const form = Object.assign({}, state.userRegister, {
        emailPlaceholder,
        passwordPlaceholder,
        provider: action.provider,
      });
      return Object.assign({}, state, { userRegister: form });
    }
    case 'LOGOUT':
      return Object.assign({}, state, initialState);
    case 'UPDATE_DEVELOPER': {
      const form = Object.assign({}, state.developerDashboard,
        {
          serviceName: action.developer.serviceName,
          serviceDescription: action.developer.serviceDescription,
          callbackUrl: action.developer.callbackUrl,
          localCallback: action.developer.localCallback,
          production: action.developer.production,
          uid: action.developer.uid,
        });
      return Object.assign({}, state, { developerDashboard: form });
    }
    default:
      return state;
  }
};
